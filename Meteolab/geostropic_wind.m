function [Ug,Vg]=geostropic_wind(H,dx,dy,f)
%% 功能：计算地转风
%作者：DY
%使用方法：
%输入位势高度（gpm）、水平格点间距、科氏参数
%输出地转风
%%=============================开始计算==================================%%
g = 9.8;                     %重力加速度
[Hx,Hy]=gradient_2d(H,dx,dy);%计算位势高度梯度
Ug=-g*Hy./f;                 %纬向地转风分量
Vg=g*Hx./f;                  %经向地转风分量
